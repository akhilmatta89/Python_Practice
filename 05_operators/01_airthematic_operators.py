x = 10
y = 20


""" Follows BODMASS rule"""

# Addition Operator
print(x + y)

# Subtraction Operator
print(x - y)

# Multiplication Operator
print(x * y)

# Division Operator (Divides left operand with right operand)
print(x / y)

# Modulus Operator(gives remainder of division)
print(x % y)

# Exponential Operator (power)
print(x ** y)

# ineger division Operator
print(x // y)

