x = 10
y = 20

""" Follows BODMAS rule"""
"""Brackets, Orders(power and exponents), divide, multiply, addition, subtraction"""

# Addition Operator
print(x + y)

# Subtraction Operator
print(x - y)

# Multiplication Operator
print(x * y)

# Division Operator (Divides left operand with right operand)
print(x / y)  # single slash (/) performs standard division and returns a decimal (float)

# Modulus Operator(gives remainder of division)
print(x % y)

# Exponential Operator (power)
print(x ** y)

# floor division Operator
print(x // y)  # the double slash performs division and then rounds down the result to the nearest whole number
